    public void obtenerDatos() {

        List<Revista> revista = new DAORevista().ObtenerDatos();

        DefaultTableModel modelo = new DefaultTableModel();

        String[] columnas = {"Numero", "Titulo", "Año", "ISSN", "Precio", "Hora Venta"};

        modelo.setColumnIdentifiers(columnas);

        for (Revista rv : revista) {

            String[] renglon = {Integer.toString(rv.getNumero()), rv.getTitulo(),
                Integer.toString(rv.getAyo()),
                rv.getIssn(),
                Float.toString(rv.getPrecio()),
                rv.getHoraventa().toString()};
            modelo.addRow(renglon);
        }
        tableRevista.setModel(modelo);
    }

    public void actualizarRevista() {
        int num = Integer.parseInt(this.jtxtNumero.getText());
        String ti = this.jtxtTitulo.getText();
        int ay = Integer.parseInt(this.jtxtAyo.getText());
        float pre = Float.parseFloat(this.jtxtPrecio.getText());
        String iss = this.jtxtIssn.getText();
        java.sql.Time hov = java.sql.Time.valueOf(this.jtxtHoraVenta.getText());

        DAORevista dao = new DAORevista();
        int res = dao.Actualizar(num, ti, ay, iss, pre, hov);
        if (res == 1) {
            JOptionPane.showMessageDialog(rootPane, "Revista Actualizada.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un ERROR.");
        }
    }

