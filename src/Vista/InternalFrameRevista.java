    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int fila = this.tableRevista.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla.");
        } else {
            JDialog.setDefaultLookAndFeelDecorated(true);
            int resp = JOptionPane.showConfirmDialog(null, "¿Esta seguro de Eliminar?", "Aceptar",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (resp == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(rootPane, "Registro no borrado.");
            } else {
                if (resp == JOptionPane.YES_OPTION) {
                    int num = Integer.parseInt((String) this.tableRevista.getValueAt(fila, 0).toString());
                    DAORevista dao = new DAORevista();
                    dao.Eliminar(num);
                    obtenerDatos();
                }
            }
            if (resp == JOptionPane.CLOSED_OPTION) {
                JOptionPane.showMessageDialog(rootPane, "Ninguna accion realizada.");
            }
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

