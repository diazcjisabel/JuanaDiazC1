    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String tit = jtxtTitulo.getText();
        String ay = jtxtAyo.getText();
        String hov = jtxtHoraVenta.getText();
        String iss = jtxtIssn.getText();
        String pre = jtxtPrecio.getText();

        if (tit.contentEquals("") || ay.contentEquals("") || hov.contentEquals("") || iss.contentEquals("")
                || pre.contentEquals("")) {

            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios.");

        } else {

            try {

                java.sql.Time ti = java.sql.Time.valueOf(hov);
                int a = Integer.parseInt(ay);
                float p = Float.parseFloat(pre);

                Revista rv = new DAORevista().Insertar(tit, a, iss, p, ti);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado.");

            } catch (Exception e) {

                e.printStackTrace();
                System.out.println("No se agrego el registro.");
            }
        }
        obtenerDatos();
        LimpiarCampos();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = this.tableRevista.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla.");
        } else {
            try {

                int num = Integer.parseInt((String) this.tableRevista.getValueAt(fila, 0));
                String ti = (String) this.tableRevista.getValueAt(fila, 1);
                int ay = Integer.parseInt((String) this.tableRevista.getValueAt(fila, 2));
                String iss = (String) this.tableRevista.getValueAt(fila, 3);
                float pre = Float.parseFloat((String) this.tableRevista.getValueAt(fila, 4));
                java.sql.Time hov = Time.valueOf((String) this.tableRevista.getValueAt(fila, 5).toString());

                jtxtNumero.setText("" + num);
                jtxtTitulo.setText(ti);
                jtxtAyo.setText(String.valueOf(ay));
                jtxtHoraVenta.setText(String.valueOf(hov));
                jtxtIssn.setText(iss);
                jtxtPrecio.setText(String.valueOf(pre));

            } catch (NumberFormatException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

